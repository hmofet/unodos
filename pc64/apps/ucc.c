static void scope_push(Cc *cc)
{
    Scope *s = ucc_alloc(cc, sizeof *s);
    s->syms = 0; s->tags = 0; s->up = cc->scope;
    cc->scope = s;
}
static void scope_pop(Cc *cc) { cc->scope = cc->scope->up; }

static Sym *sym_find(Cc *cc, const char *p, int len)
{
    Scope *sc; Sym *s;
    for (sc = cc->scope; sc; sc = sc->up)
        for (s = sc->syms; s; s = s->next)
            if ((int)strlen(s->name) == len && !strncmp(s->name, p, (unsigned long)len))
                return s;
    return 0;
}
static Sym *tag_find(Cc *cc, const char *p, int len)
{
    Scope *sc; Sym *s;
    for (sc = cc->scope; sc; sc = sc->up)
        for (s = sc->tags; s; s = s->next)
            if ((int)strlen(s->name) == len && !strncmp(s->name, p, (unsigned long)len))
                return s;
    return 0;
}

static Sym *sym_new(Cc *cc, Tok *id, Type *ty, int cls)
{
    Sym *s = ucc_alloc(cc, sizeof *s);
    char *nm = ucc_alloc(cc, id->len + 1);
    memset(s, 0, sizeof *s);
    memcpy(nm, id->p, (unsigned long)id->len); nm[id->len] = 0;
    s->name = nm; s->ty = ty; s->cls = (u8)cls; s->impno = -1;
    s->next = cc->scope->syms; cc->scope->syms = s;
    return s;
}

