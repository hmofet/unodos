static Type *ty_ptr(Cc *cc, Type *base)
{
    Type *t = ty_new(cc, TY_PTR, 8, 8);
    t->base = base;
    return t;
}
static Type *ty_arr(Cc *cc, Type *base, int n)
{
    Type *t = ty_new(cc, TY_ARR, n >= 0 ? base->size * n : 0, base->align);
    t->base = base; t->arr_len = n;
    return t;
}

int ucc_type_is_int(const Type *t)
{
    return t->kind == TY_CHAR || t->kind == TY_SHORT ||
           t->kind == TY_INT  || t->kind == TY_LONG;
}

