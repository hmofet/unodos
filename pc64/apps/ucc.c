/* ===========================================================================
 * ucc front end - preprocessor, lexer, parser, types, constant folding.
 *
 * UnoC is the C subset documented in docs_esp/LANG.MD: the integer types,
 * pointers/arrays/structs/unions/enums/typedefs/function pointers, the full
 * C expression and statement set, constant initializers (including brace
 * and address-constant forms), and a minimal preprocessor (#include one
 * file deep, object-like #define, #ifdef/#ifndef/#else/#endif).  Left out
 * by design: floats, varargs, bitfields, function-like macros, goto, VLAs.
 *
 * Written from scratch for UnoDOS (CC BY-NC, like the rest of the repo).
 * The structure follows the classic single-pass recursive-descent shape;
 * everything allocates from the caller's work arena and bails out through
 * one setjmp on hard errors, so the compiler needs no OS services at all -
 * the same source builds freestanding inside STUDIO.UNO and hosted for the
 * tools/ucc_test.c battery.
 * ======================================================================== */
#include "ucc_int.h"

/* the legal import list, generated by build.sh from pc64_modload.c (KX names)
 * into build/apps/ucc_kexports.h:  static const char *const kKexports[] and
 * KEXPORTS_N.  Host builds generate the same file (see tools/ucc_test.sh). */
#include UCC_KEXPORTS_H

int ucc_import_ok(const char *name, int len)
{
    int i;
    for (i = 0; i < KEXPORTS_N; i++)
        if ((int)strlen(kKexports[i]) == len &&
            !strncmp(kKexports[i], name, (unsigned long)len)) return 1;
    return 0;
}

/* ---- arena / diagnostics -------------------------------------------------- */
void *ucc_alloc(Cc *cc, long n)
{
    void *p;
    n = (n + 15) & ~15L;
    if (cc->wkuse + n > cc->wkcap) ucc_fatal(cc, "out of compiler memory", 0);
    p = cc->wk + cc->wkuse; cc->wkuse += n;
    return p;
}

static void dcpy(char *dst, int cap, int *pos, const char *s)
{
    while (s && *s && *pos < cap - 1) dst[(*pos)++] = *s++;
    dst[*pos] = 0;
}

static void ucc_diag_at(Cc *cc, const char *file, int line, int col,
                        const char *m1, const char *m2)
{
    if (cc->ndiag < cc->maxdiag) {
        UccDiag *d = &cc->diag[cc->ndiag];
        int pos = 0;
        d->line = line; d->col = col;
        d->file[0] = 0;
        if (file) { int fp = 0; dcpy(d->file, (int)sizeof d->file, &fp, file); }
        dcpy(d->msg, (int)sizeof d->msg, &pos, m1);
        if (m2) { dcpy(d->msg, (int)sizeof d->msg, &pos, " '");
                  dcpy(d->msg, (int)sizeof d->msg, &pos, m2);
                  dcpy(d->msg, (int)sizeof d->msg, &pos, "'"); }
    }
    cc->ndiag++;
}

void ucc_fatal(Cc *cc, const char *m1, const char *m2)
{
    ucc_diag_at(cc, 0, 0, 0, m1, m2);
    __builtin_longjmp(cc->jb, 1);
}

/* copy a token's spelling into a small stack buffer for messages */
static const char *tok_spell(Cc *cc, Tok *t, char *buf, int cap)
{
    static const char *kPunct[P_NPUNCT] = {
        "(", ")", "{", "}", "[", "]", ";", ",", ".", "->", "?", ":", "...",
        "+", "-", "*", "/", "%", "&", "|", "^", "~", "!", "<", ">", "<=",
        ">=", "==", "!=", "<<", ">>", "&&", "||", "++", "--", "=",
        "+=", "-=", "*=", "/=", "%=", "&=", "|=", "^=", "<<=", ">>=",
    };
    static const char *kKw[KW_NKW] = {
        "void", "char", "short", "int", "long", "unsigned", "signed",
        "const", "static", "extern", "struct", "union", "enum", "typedef",
        "if", "else", "while", "do", "for", "switch", "case", "default",
        "break", "continue", "return", "sizeof",
    };
    int pos = 0;
    (void)cc;
    if (!t) return "end of file";
    switch (t->kind) {
    case TK_EOF:   return "end of file";
    case TK_PUNCT: return kPunct[t->id];
    case TK_KW:    return kKw[t->id];
    case TK_NUM:   return "number";
    case TK_STR:   return "string literal";
    default:
        if (t->len >= cap) return "identifier";
        while (pos < t->len) { buf[pos] = t->p[pos]; pos++; }
        buf[pos] = 0;
        return buf;
    }
}

void ucc_err_tok(Cc *cc, Tok *t, const char *m1, const char *m2)
{
    const char *fn = (t && t->file < (u16)cc->nfiles) ? cc->files[t->file] : 0;
    ucc_diag_at(cc, fn, t ? t->line : 0, t ? t->col : 0, m1, m2);
    __builtin_longjmp(cc->jb, 1);
}

/* ============================================================================
 * LEXER (+ the directive layer: the two live in one loop because directives
 * are line-oriented while tokens are not)
 * ========================================================================== */
/* a macro is visible to tokens lexed in [defseq, undefseq); redefinition
 * simply prepends a fresh entry with a later window */
typedef struct Macro Macro;
struct Macro {
    const char *name; int len;
    Tok *body;
    int  defseq, undefseq;
    u8   busy;
    Macro *next;
};

typedef struct Lx {
    Cc *cc;
    const char *p, *end;
    int line, col;
    u16 file;
    int bol;                    /* at beginning of line (only ws so far) */
} Lx;

static int lx_peek(Lx *L)      { return L->p < L->end ? (u8)*L->p : -1; }
static int lx_at(Lx *L, int i) { return L->p + i < L->end ? (u8)L->p[i] : -1; }
static int lx_adv(Lx *L)
{
    int c = lx_peek(L);
    if (c < 0) return c;
    L->p++;
    if (c == '\n') { L->line++; L->col = 1; L->bol = 1; }
    else           { L->col++; if (c != ' ' && c != '\t' && c != '\r') L->bol = 0; }
    return c;
}

/* skip spaces + comments; if stop_nl, stop before a newline (directive mode) */
static void lx_space(Lx *L, int stop_nl)
{
    for (;;) {
        int c = lx_peek(L);
        if (c == ' ' || c == '\t' || c == '\r') { lx_adv(L); continue; }
        if (c == '\n') { if (stop_nl) return; lx_adv(L); continue; }
        if (c == '/' && lx_at(L, 1) == '/') {
            while (lx_peek(L) >= 0 && lx_peek(L) != '\n') lx_adv(L);
            continue;
        }
        if (c == '/' && lx_at(L, 1) == '*') {
            lx_adv(L); lx_adv(L);
            for (;;) {
                if (lx_peek(L) < 0) ucc_fatal(L->cc, "unterminated comment", 0);
                if (lx_peek(L) == '*' && lx_at(L, 1) == '/') { lx_adv(L); lx_adv(L); break; }
                lx_adv(L);
            }
            continue;
        }
        return;
    }
}

static int is_ident0(int c) { return (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || c == '_'; }
static int is_digit(int c)  { return c >= '0' && c <= '9'; }
static int is_ident(int c)  { return is_ident0(c) || is_digit(c); }

static int kw_lookup(const char *p, int len)
{
    static const struct { const char *s; int id; } kw[] = {
        {"void",KW_VOID},{"char",KW_CHAR},{"short",KW_SHORT},{"int",KW_INT},
        {"long",KW_LONG},{"unsigned",KW_UNSIGNED},{"signed",KW_SIGNED},
        {"const",KW_CONST},{"static",KW_STATIC},{"extern",KW_EXTERN},
        {"struct",KW_STRUCT},{"union",KW_UNION},{"enum",KW_ENUM},
        {"typedef",KW_TYPEDEF},{"if",KW_IF},{"else",KW_ELSE},
        {"while",KW_WHILE},{"do",KW_DO},{"for",KW_FOR},{"switch",KW_SWITCH},
        {"case",KW_CASE},{"default",KW_DEFAULT},{"break",KW_BREAK},
        {"continue",KW_CONTINUE},{"return",KW_RETURN},{"sizeof",KW_SIZEOF},
    };
    int i;
    for (i = 0; i < (int)(sizeof kw / sizeof kw[0]); i++)
        if ((int)strlen(kw[i].s) == len && !strncmp(kw[i].s, p, (unsigned long)len))
            return kw[i].id;
    return -1;
}

static int esc_char(Lx *L)
{
    int c = lx_adv(L);
    switch (c) {
    case 'n': return '\n';  case 't': return '\t';  case 'r': return '\r';
    case '0': return 0;     case '\\': return '\\'; case '\'': return '\'';
    case '"': return '"';   case 'b': return '\b';  case 'f': return '\f';
    case 'v': return '\v';  case 'a': return 7;
    case 'x': {
        int v = 0, n = 0;
        for (;;) {
            int d = lx_peek(L);
            if (d >= '0' && d <= '9') d -= '0';
            else if (d >= 'a' && d <= 'f') d -= 'a' - 10;
            else if (d >= 'A' && d <= 'F') d -= 'A' - 10;
            else break;
            v = v * 16 + d; lx_adv(L); n++;
        }
        if (!n) ucc_fatal(L->cc, "bad \\x escape", 0);
        return v & 0xFF;
    }
    default:
        if (c >= '1' && c <= '7') {           /* octal */
            int v = c - '0';
            while (lx_peek(L) >= '0' && lx_peek(L) <= '7')
                v = v * 8 + (lx_adv(L) - '0');
            return v & 0xFF;
        }
        ucc_fatal(L->cc, "bad escape sequence", 0);
        return 0;
    }
}

static Tok *tok_new(Cc *cc, int kind, Lx *L, int line, int col)
{
    Tok *t = ucc_alloc(cc, sizeof *t);
    memset(t, 0, sizeof *t);
    t->kind = (u8)kind; t->file = L ? L->file : 0;
    t->line = line; t->col = col;
    t->seq = ++cc->lexseq;
    return t;
}

/* lex exactly one token (directives already handled by the caller);
 * returns 0 at end of input */
static Tok *lx_one(Lx *L, int stop_nl)
{
    Cc *cc = L->cc;
    int line, col, c;
    Tok *t;

    lx_space(L, stop_nl);
    if (stop_nl && (lx_peek(L) == '\n' || lx_peek(L) < 0)) return 0;
    if (lx_peek(L) < 0) return 0;
    line = L->line; col = L->col;
    c = lx_peek(L);

    if (is_ident0(c)) {
        const char *s = L->p;
        int n = 0, kw;
        while (is_ident(lx_peek(L))) { lx_adv(L); n++; }
        kw = kw_lookup(s, n);
        if (kw >= 0) { t = tok_new(cc, TK_KW, L, line, col); t->id = (u8)kw; }
        else {
            char *cp = ucc_alloc(cc, n + 1);
            memcpy(cp, s, (unsigned long)n); cp[n] = 0;
            t = tok_new(cc, TK_IDENT, L, line, col);
            t->p = cp; t->len = n;
        }
        return t;
    }

    if (is_digit(c)) {
        u64 v = 0;
        t = tok_new(cc, TK_NUM, L, line, col);
        if (c == '0' && (lx_at(L, 1) == 'x' || lx_at(L, 1) == 'X')) {
            lx_adv(L); lx_adv(L);
            for (;;) {
                int d = lx_peek(L);
                if (d >= '0' && d <= '9') d -= '0';
                else if (d >= 'a' && d <= 'f') d -= 'a' - 10;
                else if (d >= 'A' && d <= 'F') d -= 'A' - 10;
                else break;
                v = v * 16 + (u64)d; lx_adv(L);
            }
        } else if (c == '0') {
            while (lx_peek(L) >= '0' && lx_peek(L) <= '7')
                v = v * 8 + (u64)(lx_adv(L) - '0');
        } else {
            while (is_digit(lx_peek(L)))
                v = v * 10 + (u64)(lx_adv(L) - '0');
        }
        for (;;) {                              /* suffixes, any order */
            int d = lx_peek(L);
            if (d == 'u' || d == 'U') { t->num_uns = 1; lx_adv(L); }
            else if (d == 'l' || d == 'L') { t->num_long++; lx_adv(L); }
            else break;
        }
        t->val = (i64)v;
        return t;
    }

    if (c == '\'') {
        int v;
        lx_adv(L);
        c = lx_adv(L);
        if (c == '\\') v = esc_char(L); else v = c;
        if (lx_adv(L) != '\'') ucc_fatal(cc, "unterminated char literal", 0);
        t = tok_new(cc, TK_NUM, L, line, col);
        t->val = (signed char)v;
        return t;
    }

    if (c == '"') {
        /* decode into the arena; worst case = source length */
        char *buf = ucc_alloc(cc, (long)(L->end - L->p) + 1);
        int n = 0;
        lx_adv(L);
        for (;;) {
            c = lx_peek(L);
            if (c < 0 || c == '\n') ucc_fatal(cc, "unterminated string", 0);
            lx_adv(L);
            if (c == '"') break;
            if (c == '\\') c = esc_char(L);
            buf[n++] = (char)c;
        }
        buf[n] = 0;
        t = tok_new(cc, TK_STR, L, line, col);
        t->str = buf; t->slen = n;
        return t;
    }

    /* punctuators - longest match first */
    {
        static const struct { const char *s; int id; } px[] = {
            {"...",P_ELLIPSIS},{"<<=",P_SHLA},{">>=",P_SHRA},
            {"->",P_ARROW},{"++",P_INC},{"--",P_DEC},{"<<",P_SHL},{">>",P_SHR},
            {"<=",P_LE},{">=",P_GE},{"==",P_EQ},{"!=",P_NE},{"&&",P_ANDAND},
            {"||",P_OROR},{"+=",P_ADDA},{"-=",P_SUBA},{"*=",P_MULA},
            {"/=",P_DIVA},{"%=",P_MODA},{"&=",P_ANDA},{"|=",P_ORA},{"^=",P_XORA},
            {"(",P_LPAR},{")",P_RPAR},{"{",P_LBRACE},{"}",P_RBRACE},
            {"[",P_LBRACK},{"]",P_RBRACK},{";",P_SEMI},{",",P_COMMA},
            {".",P_DOT},{"?",P_QUEST},{":",P_COLON},{"+",P_PLUS},{"-",P_MINUS},
            {"*",P_STAR},{"/",P_SLASH},{"%",P_PCT},{"&",P_AMP},{"|",P_PIPE},
            {"^",P_CARET},{"~",P_TILDE},{"!",P_BANG},{"<",P_LT},{">",P_GT},
            {"=",P_ASSIGN},
        };
        int i;
        for (i = 0; i < (int)(sizeof px / sizeof px[0]); i++) {
            int n = (int)strlen(px[i].s), j, hit = 1;
            for (j = 0; j < n; j++)
                if (lx_at(L, j) != px[i].s[j]) { hit = 0; break; }
            if (hit) {
                for (j = 0; j < n; j++) lx_adv(L);
                t = tok_new(cc, TK_PUNCT, L, line, col);
                t->id = (u8)px[i].id;
                return t;
            }
        }
    }
    ucc_fatal(cc, "unexpected character in source", 0);
    return 0;
}

/* ---- directives + include splicing ---------------------------------------- */
/* the macro visible to a token lexed at sequence position seq (entries are
 * prepended, so the first window hit is the latest matching definition) */
static Macro *macro_find(Cc *cc, const char *p, int len, int seq)
{
    Macro *m;
    for (m = cc->macros; m; m = m->next)
        if (m->len == len && !strncmp(m->name, p, (unsigned long)len) &&
            seq >= m->defseq && seq < m->undefseq)
            return m;
    return 0;
}

#define UCC_INC_DEPTH 4
#define UCC_INC_MAX   (192 * 1024L)

static Tok *lex_unit(Cc *cc, const char *src, long len, const char *name,
                     UccIncRead inc, void *incctx, int depth, Tok ***tailp);

/* handle one # directive; L is positioned after the '#' */
static Tok *directive(Cc *cc, Lx *L, UccIncRead inc, void *incctx, int depth,
                      Tok ***tailp, int *skip, int *skdepth, int *sksat)
{
    Tok *d = lx_one(L, 1);
    char nm[24];
    const char *dn;

    if (!d) return 0;                      /* '#' alone on a line */
    dn = tok_spell(cc, d, nm, (int)sizeof nm);

    /* while skipping a dead #if region, only the structure matters */
    if (*skip) {
        if (!strcmp(dn, "ifdef") || !strcmp(dn, "ifndef")) (*skdepth)++;
        else if (!strcmp(dn, "endif")) {
            if (*skdepth) (*skdepth)--; else *skip = 0;
        } else if (!strcmp(dn, "else")) {
            if (!*skdepth && !*sksat) *skip = 0;
        }
        while (lx_one(L, 1)) {}
        return 0;
    }

    if (!strcmp(dn, "include")) {
        Tok *f = lx_one(L, 1);
        char *ibuf; long n;
        Tok *head, **tail2;
        if (!f || f->kind != TK_STR)
            ucc_err_tok(cc, f ? f : d, "#include expects \"FILE\"", 0);
        if (depth >= UCC_INC_DEPTH)
            ucc_err_tok(cc, f, "#include nested too deep", 0);
        if (!inc)
            ucc_err_tok(cc, f, "no include files available here", 0);
        ibuf = ucc_alloc(cc, UCC_INC_MAX);
        n = inc(incctx, f->str, ibuf, UCC_INC_MAX);
        if (n < 0)
            ucc_err_tok(cc, f, "include file not found", f->str);
        while (lx_one(L, 1)) {}
        head = lex_unit(cc, ibuf, n, f->str, inc, incctx, depth + 1, &tail2);
        if (head) { **tailp = head; *tailp = tail2; }
        return 0;
    }
    if (!strcmp(dn, "define")) {
        Tok *id = lx_one(L, 1), *b, **bt;
        Macro *m;
        if (!id || id->kind != TK_IDENT)
            ucc_err_tok(cc, id ? id : d, "#define expects a name", 0);
        if (lx_peek(L) == '(')
            ucc_err_tok(cc, id, "function-like macros are not in UnoC", 0);
        m = ucc_alloc(cc, sizeof *m);
        m->name = id->p; m->len = id->len; m->busy = 0; m->body = 0;
        m->undefseq = 0x7FFFFFFF;
        bt = &m->body;
        while ((b = lx_one(L, 1)) != 0) { *bt = b; bt = &b->next; }
        m->defseq = cc->lexseq + 1;        /* visible from the next token on */
        m->next = cc->macros; cc->macros = m;
        return 0;
    }
    if (!strcmp(dn, "undef")) {
        Tok *id = lx_one(L, 1);
        Macro *m;
        if (id && id->kind == TK_IDENT) {
            m = macro_find(cc, id->p, id->len, cc->lexseq + 1);
            if (m) m->undefseq = cc->lexseq + 1;
        }
        while (lx_one(L, 1)) {}
        return 0;
    }
    if (!strcmp(dn, "ifdef") || !strcmp(dn, "ifndef")) {
        Tok *id = lx_one(L, 1);
        int def;
        if (!id || id->kind != TK_IDENT)
            ucc_err_tok(cc, id ? id : d, "#ifdef expects a name", 0);
        def = macro_find(cc, id->p, id->len, cc->lexseq + 1) != 0;
        if (!strcmp(dn, "ifndef")) def = !def;
        while (lx_one(L, 1)) {}
        if (def) { *skip = 0; *sksat = 1; }     /* branch taken */
        else     { *skip = 1; *skdepth = 0; *sksat = 0; }
        return 0;
    }
    if (!strcmp(dn, "else"))  {                 /* taken branch done */
        while (lx_one(L, 1)) {}
        *skip = 1; *skdepth = 0; *sksat = 1;
        return 0;
    }
    if (!strcmp(dn, "endif")) { while (lx_one(L, 1)) {} *sksat = 0; return 0; }
    if (!strcmp(dn, "pragma")) { while (lx_one(L, 1)) {} return 0; }
    if (!strcmp(dn, "error"))
        ucc_err_tok(cc, d, "#error directive reached", 0);
    if (!strcmp(dn, "if") || !strcmp(dn, "elif"))
        ucc_err_tok(cc, d, "#if expressions are not in UnoC (use #ifdef)", 0);
    ucc_err_tok(cc, d, "unknown preprocessor directive", dn);
    return 0;
}

/* lex a whole file (recursing into includes); appends to a fresh list.
 * Conditional-skip state is per-file, which UnoC can afford because its
 * conditionals are include-guard-grade, not cross-file. */
static Tok *lex_unit(Cc *cc, const char *src, long len, const char *name,
                     UccIncRead inc, void *incctx, int depth, Tok ***tailp)
{
    Lx L;
    Tok *head = 0, **tail = &head, *t;
    int skip = 0, skdepth = 0, sksat = 0;

    if (cc->nfiles >= UCC_MAXFILES) ucc_fatal(cc, "too many include files", 0);
    { char *nm = ucc_alloc(cc, (long)strlen(name) + 1);
      strcpy(nm, name); cc->files[cc->nfiles] = nm; }

    L.cc = cc; L.p = src; L.end = src + len;
    L.line = 1; L.col = 1; L.file = (u16)cc->nfiles++; L.bol = 1;

    for (;;) {
        lx_space(&L, 0);
        if (lx_peek(&L) < 0) break;
        if (lx_peek(&L) == '#' && L.bol) {
            lx_adv(&L);
            directive(cc, &L, inc, incctx, depth, &tail, &skip, &skdepth, &sksat);
            continue;
        }
        if (skip) { lx_adv(&L); continue; }
        t = lx_one(&L, 0);
        if (!t) break;
        *tail = t; tail = &t->next;
    }
    if (skip) ucc_fatal(cc, "missing #endif", name);
    *tailp = tail;
    return head;
}

/* ---- macro expansion (object-like, recursive with busy flags) ------------- */
static Tok *tok_dup(Cc *cc, Tok *t, Tok *site)
{
    Tok *n = ucc_alloc(cc, sizeof *n);
    *n = *t;
    n->next = 0;
    /* report diagnostics (and any later rescans) at the use site */
    n->file = site->file; n->line = site->line; n->col = site->col;
    n->seq = site->seq;
    return n;
}

/* splice a macro body at a use site; nested names rescan against the table
 * as visible AT THE USE SITE (site->seq), per cpp rescanning semantics */
static Tok *expand_list(Cc *cc, Tok *in, Tok *site, int depth)
{
    Tok *head = 0, **tail = &head, *t;
    if (depth > 32) ucc_fatal(cc, "macro expansion too deep", 0);
    for (t = in; t; t = t->next) {
        Macro *m = (t->kind == TK_IDENT)
                 ? macro_find(cc, t->p, t->len, site->seq) : 0;
        if (m && !m->busy) {
            Tok *ex;
            m->busy = 1;
            ex = expand_list(cc, m->body, site, depth + 1);
            m->busy = 0;
            while (ex) { Tok *nx = ex->next; ex->next = 0; *tail = ex; tail = &ex->next; ex = nx; }
        } else {
            Tok *cp = tok_dup(cc, t, site);
            *tail = cp; tail = &cp->next;
        }
    }
    return head;
}

/* expansion over the top-level stream: tokens are already fresh, so only
 * macro hits allocate copies */
static Tok *expand_stream(Cc *cc, Tok *in)
{
    Tok *head = 0, **tail = &head, *t, *nx;
    for (t = in; t; t = nx) {
        Macro *m = (t->kind == TK_IDENT)
                 ? macro_find(cc, t->p, t->len, t->seq) : 0;
        nx = t->next;
        if (m && !m->busy) {
            Tok *ex;
            m->busy = 1;
            ex = expand_list(cc, m->body, t, 1);
            m->busy = 0;
            while (ex) { Tok *x = ex->next; ex->next = 0; *tail = ex; tail = &ex->next; ex = x; }
        } else {
            t->next = 0;
            *tail = t; tail = &t->next;
        }
    }
    {
        Tok *eof = ucc_alloc(cc, sizeof *eof);
        memset(eof, 0, sizeof *eof);
        eof->kind = TK_EOF;
        *tail = eof;
    }
    return head;
}

