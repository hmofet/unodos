/* ---- state ------------------------------------------------------------------ */
static int   cl_utc_off = 0;            /* local time = UTC + this, in MINUTES */
static char  cl_items[NCITY][40];
static const char *cl_ptr[NCITY];
static char  cl_digital[48];
