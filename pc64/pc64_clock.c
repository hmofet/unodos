/* ===========================================================================
 * UnoDOS/pc64 - Clock: an analog face, a world map with the day/night
 * terminator, and a world-time list.
 *
 * Replaces the old two-label Clock (a digital string and "(firmware RTC)").
 *
 * Two canvases, both earning it: an analog dial is per-pixel geometry, and a
 * shaded world map is per-pixel by definition. Everything around them - the
 * UTC-offset spinner, the city list, the digital readout - is ordinary unoui
 * widgets, so the whole app follows the theme.
 *
 * ---- the terminator --------------------------------------------------------
 * Day/night is real spherical geometry, not a drawn curve. The subsolar point
 * is at longitude 180 - 15*UTC_hours and at a latitude equal to the solar
 * declination, which is approximated from the day of year by
 *   decl = 23.44 deg * sin(360/365 * (N + 284))
 * A point is in daylight when
 *   sin(lat)sin(decl) + cos(lat)cos(decl)cos(lon - sublon) > 0
 * which is evaluated per map pixel. That gets the seasons right for free: the
 * Arctic is lit around the June solstice and dark around December, and the
 * terminator tilts rather than running straight down the map.
 *
 * ---- what the RTC gives us --------------------------------------------------
 * The firmware RTC has no time zone attached, so nothing here can know the
 * machine's offset from UTC. Rather than guess, the app asks: the "UTC offset"
 * spinner states what local time IS, and every world time is derived from
 * it. Default 0 - i.e. "the RTC is UTC" - which is what a machine with no
 * other information should assume. There is no DST handling and the UI says
 * so; DST rules are a political database, not arithmetic, and shipping a
 * half-right one would be worse than being explicit.
 * ======================================================================== */
